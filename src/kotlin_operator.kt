//operators in kotlin
fun main()
{
    var a:Int=100
    var b:Int=200
    //var variable
    //the value of the variable a and b can be changed in later stage of the program
    //mutable variable
    println("a+b="+(a+b))
    println("a-b="+(a-b))
    println("a*b="+(a*b))
    println("a*b="+(a/b))
    println("a%b="+(a%b))
}