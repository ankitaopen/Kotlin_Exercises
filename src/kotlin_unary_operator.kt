//kotlin unary operator
fun main(args:Array<String>)
{
    var a:Int=10
    var b:Int=5
    var flag=true
    //var variable
    //mutable variable
    //as its var variable we can change its value in the program
    println("+a:"+ +a)
    println("-b:"+ +b)
    println("++a:"+ ++a)
    println("--b:"+ --b)
    println("!flag:"+ !flag)
}