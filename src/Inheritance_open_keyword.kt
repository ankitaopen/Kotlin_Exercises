//inheritance in kotlin
//open keyword
//open---------class
//in kotlin all the class are final by default, so they cant be inherited by default
//this is totally opposite to what we have learned in java, in java we have to make the class final explicitly
//** so to make a class inheritable by other class we must mark it with open keyword ,otherwise we get an error saying type is final so cannot be inherited
//**basically the base class has to be declared with open keyword to make it inheritable
//open----------function
//just like classes all the functions in kotlin are by default final in nature
//we cannot override a function when the function is final in nature
//**function overriding is a process of redefining the function of a base class in the child class, also in the child class we have to use override modifier
