//type conversion in kotlin
//Int to Long
fun main(args:Array<String>)
{
    var value1:Int=100;
    //var variable
    //mutable variable
    println("the value of variable value1 is:"+value1)
    val value2:Long=value1.toLong()
    //val variable
    //immutable variable
    println("the variable of variable value2 is:"+value2)
    //type conversion

}