//var
//var is a mutable variable
//we can change the value of a variable declared using var keyword in later stage of the program
fun main(args : Array<String>)
{
    var variable : Int = 2000;
    println("the value of Int variable is:" +variable);
    variable = 4000;
    //reassigning a value to variable var
    println("the new value of Int variable is:" +variable);
}
//here we are able to re-assign the value to the variable var ---- as var is a mutable variable